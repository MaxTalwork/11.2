def get_upper():
    """
    Меняет все буквы на заглавные
    """
    text = input('Введите текст: ')
    print(f'{text.upper()}')


def get_title():
    """
    Меняет первые буквы на заглавные
    """
    text = input('Введите текст: ')
    print(f'{text.title()}')

get_upper()
get_title()

